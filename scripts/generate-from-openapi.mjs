#!/usr/bin/env node

/**
 * OpenAPI Code Generator for Marvin SDK and CLI
 *
 * Reads the OpenAPI spec from the Marvin backend and generates:
 * 1. TypeScript SDK modules (for marvin-sdk)
 * 2. CLI command files (for marvin-cli)
 *
 * Usage:
 *   node scripts/generate-from-openapi.mjs [openapi-url]
 *
 * Default URL: http://localhost:8080/openapi.json
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

// Configuration
const OPENAPI_URL = process.argv[2] || 'http://localhost:8080/openapi.json';
const SDK_OUTPUT_DIR = path.join(__dirname, '../../marvin-sdk/src/generated');
const CLI_OUTPUT_DIR = path.join(__dirname, '../src/generated');

// Fetch OpenAPI spec
async function fetchOpenAPISpec(url) {
  console.log(`📥 Fetching OpenAPI spec from ${url}...`);
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed to fetch OpenAPI spec: ${response.statusText}`);
  }
  return response.json();
}

// Parse endpoints by tag/group
function parseEndpoints(spec) {
  const groups = {};

  for (const [path, methods] of Object.entries(spec.paths)) {
    for (const [method, details] of Object.entries(methods)) {
      if (typeof details !== 'object' || !details.tags) continue;

      const tag = details.tags[0]; // Use first tag as group
      if (!groups[tag]) {
        groups[tag] = [];
      }

      groups[tag].push({
        path,
        method: method.toUpperCase(),
        operationId: details.operationId,
        summary: details.summary,
        parameters: details.parameters || [],
        requestBody: details.requestBody,
        responses: details.responses,
        security: details.security,
      });
    }
  }

  return groups;
}

// Generate SDK module for a group
function generateSDKModule(groupName, endpoints) {
  const moduleName = groupName.replace(/[^a-zA-Z0-9]/g, '');
  const className = `${moduleName}Module`;

  let code = `/**
 * ${groupName} Module - Auto-generated from OpenAPI spec
 *
 * DO NOT EDIT - This file is generated by scripts/generate-from-openapi.mjs
 */

import type { HttpClient } from '../core';

export class ${className} {
  constructor(private http: HttpClient) {}

`;

  // Generate method for each endpoint
  for (const endpoint of endpoints) {
    const methodName = endpoint.operationId
      .replace(/^(get|post|put|patch|delete)_/, '')
      .replace(/_api_.*/, '')
      .replace(/_/g, '_');

    // Extract path parameters
    const pathParams = endpoint.path.match(/\{([^}]+)\}/g)?.map(p => p.slice(1, -1)) || [];

    // Build method signature
    const params = pathParams.map(p => `${p}: string`).join(', ');
    const hasBody = endpoint.requestBody;
    const bodyParam = hasBody ? (params ? ', data: any' : 'data: any') : '';

    code += `  /**
   * ${endpoint.summary}
   */
  async ${methodName}(${params}${bodyParam}): Promise<any> {
    const path = \`${endpoint.path.replace(/\{([^}]+)\}/g, '${$1}')}\`;
`;

    if (endpoint.method === 'GET') {
      code += `    return this.http.get(path);\n`;
    } else if (endpoint.method === 'POST') {
      code += `    return this.http.post(path${hasBody ? ', data' : ', {}'});\n`;
    } else if (endpoint.method === 'PUT') {
      code += `    return this.http.put(path${hasBody ? ', data' : ', {}'});\n`;
    } else if (endpoint.method === 'PATCH') {
      code += `    return this.http.patch(path${hasBody ? ', data' : ', {}'});\n`;
    } else if (endpoint.method === 'DELETE') {
      code += `    return this.http.delete(path);\n`;
    }

    code += `  }\n\n`;
  }

  code += `}\n`;

  return { fileName: `${moduleName}.ts`, code };
}

// Generate CLI command for a group
function generateCLICommand(groupName, endpoints) {
  const commandName = groupName.toLowerCase().replace(/[^a-z0-9]/g, '-');

  let code = `/**
 * ${groupName} Commands - Auto-generated from OpenAPI spec
 *
 * DO NOT EDIT - This file is generated by scripts/generate-from-openapi.mjs
 */

import { Command } from "commander";
import { clientFactory } from "../../shared/clients.js";
import { renderList, renderData } from "../../output.js";
import { getOutputMode, type PlatformCommandOptions } from "../../shared/types.js";

export function register${groupName.replace(/[^a-zA-Z0-9]/g, '')}Commands(parent: Command): void {
  const group = parent
    .command("${commandName}")
    .description("${groupName} operations");

`;

  // Generate command for each endpoint
  for (const endpoint of endpoints) {
    const commandParts = endpoint.path.split('/').filter(p => p && !p.startsWith('{'));
    const cmdName = commandParts[commandParts.length - 1] || 'root';

    // Extract path parameters
    const pathParams = endpoint.path.match(/\{([^}]+)\}/g)?.map(p => p.slice(1, -1)) || [];
    const cmdArgs = pathParams.map(p => `<${p}>`).join(' ');

    code += `  group
    .command("${endpoint.method.toLowerCase()} ${cmdArgs}")
    .description("${endpoint.summary}")
    .action(async function(this: Command${pathParams.map((p, i) => `, arg${i}: string`).join('')}) {
      try {
        const opts = this.optsWithGlobals<PlatformCommandOptions>();
        const client = await clientFactory.createPlatformClient(opts);
        // TODO: Implement actual API call
        console.log("Not yet implemented");
      } catch (error) {
        console.error(error instanceof Error ? error.message : error);
        process.exitCode = 1;
      }
    });

`;
  }

  code += `}\n`;

  return { fileName: `${commandName}.ts`, code };
}

// Main execution
async function main() {
  try {
    console.log('🚀 Marvin OpenAPI Code Generator\n');

    // Fetch spec
    const spec = await fetchOpenAPISpec(OPENAPI_URL);
    console.log(`✓ Loaded spec: ${spec.info.title} v${spec.info.version}`);
    console.log(`  Endpoints: ${Object.keys(spec.paths).length}`);
    console.log(`  Schemas: ${Object.keys(spec.components?.schemas || {}).length}\n`);

    // Parse endpoints by group
    const groups = parseEndpoints(spec);
    console.log(`📊 Found ${Object.keys(groups).length} endpoint groups:\n`);

    for (const [group, endpoints] of Object.entries(groups)) {
      console.log(`  - ${group}: ${endpoints.length} endpoints`);
    }

    console.log('\n🔧 Code generation options:');
    console.log('  1. Generate SDK modules');
    console.log('  2. Generate CLI commands');
    console.log('  3. Generate both');
    console.log('\nFor now, this is a proof-of-concept generator.');
    console.log('Next step: Implement full generation logic.\n');

    // Example: Generate for one group
    const platformGroups = Object.entries(groups).filter(([name]) => name.startsWith('Platform:'));

    if (platformGroups.length > 0) {
      const [groupName, endpoints] = platformGroups[0];
      console.log(`\n📝 Example: Generating code for "${groupName}"...\n`);

      const sdkModule = generateSDKModule(groupName, endpoints);
      console.log(`Generated SDK module: ${sdkModule.fileName}`);
      console.log(`Lines of code: ${sdkModule.code.split('\n').length}`);

      const cliCommand = generateCLICommand(groupName, endpoints);
      console.log(`Generated CLI command: ${cliCommand.fileName}`);
      console.log(`Lines of code: ${cliCommand.code.split('\n').length}`);

      console.log('\n✓ Code generation successful!\n');
    }

  } catch (error) {
    console.error('❌ Error:', error.message);
    process.exit(1);
  }
}

main();
